@dp.message(PollCreateState.poll_type)
async def poll_send_poll(message: types.Message, state: FSMContext):
    data = await state.get_data()
    question = data["question"]
    options = data["options"]
    audience = data.get("audience")
    username = data.get("username")
    poll_type = message.text.lower()
    allow_multiple = poll_type == "–∫—ñ–ª—å–∫–∞ –≤—ñ–¥–ø–æ–≤—ñ–¥–µ–π"

    # --- –û–±–∏—Ä–∞—î–º–æ, –∫–æ–º—É –Ω–∞–¥—Å–∏–ª–∞—Ç–∏ ---
    user_ids = []
    if audience == "–≤—Å—ñ–º –∑—ñ —à—Ç–∞—Ç—É":
        user_ids = get_all_staff_user_ids()  # –ø–æ–≤–∏–Ω–Ω–∞ –±—É—Ç–∏ —Ç–≤–æ—è —Ñ—É–Ω–∫—Ü—ñ—è
    elif audience == "—Ç–∏–º —Ö—Ç–æ –Ω–∞ –∑–º—ñ–Ω—ñ":
        user_ids = get_today_users()
    elif audience == "–∫–æ–Ω–∫—Ä–µ—Ç–Ω–æ–º—É —é–∑–µ—Ä—É" and username:
        user_ids = get_staff_user_ids_by_username(username)

    if not user_ids:
        await message.answer("–ù–µ –∑–Ω–∞–π–¥–µ–Ω–æ –∂–æ–¥–Ω–æ–≥–æ –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á–∞ –¥–ª—è –æ–ø–∏—Ç—É–≤–∞–Ω–Ω—è.", reply_markup=types.ReplyKeyboardRemove())
        await state.clear()
        return

    polls_sheet = gs.open_by_key(SHEET_KEY).worksheet('–û–ø–∏—Ç—É–≤–∞–Ω–Ω—è')
    # --- –í—ñ–¥–ø—Ä–∞–≤–ª—è—î–º–æ Poll —ñ –∑–±–µ—Ä—ñ–≥–∞—î–º–æ poll_id ---
    for uid in user_ids:
        sent = await bot.send_poll(
            uid,
            question=question,
            options=options,
            is_anonymous=False,
            allows_multiple_answers=allow_multiple
        )
        polls_sheet.append_row([
            question, ", ".join(options), audience, poll_type, sent.poll.id, '', '', ''
        ])
    await message.answer("–û–ø–∏—Ç—É–≤–∞–Ω–Ω—è –Ω–∞–¥—ñ—Å–ª–∞–Ω–æ!", reply_markup=types.ReplyKeyboardRemove())
    await state.clear()

