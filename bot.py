@dp.poll_answer()
async def save_poll_answer(poll_answer: types.PollAnswer):
    user_id = poll_answer.user.id
    username = poll_answer.user.username or str(user_id)
    selected = poll_answer.option_ids  # —Å–ø–∏—Å–æ–∫ —ñ–Ω–¥–µ–∫—Å—ñ–≤ –æ–±—Ä–∞–Ω–∏—Ö –≤–∞—Ä—ñ–∞–Ω—Ç—ñ–≤
    poll_id = poll_answer.poll_id

    # --- –ó–±–µ—Ä—ñ–≥–∞—î–º–æ —É —Ç–∞–±–ª–∏—Ü—é (–∑–Ω–∞–π—Ç–∏ —Ä—è–¥–æ–∫ –∑–∞ poll_id) ---
    polls_sheet = gs.open_by_key(SHEET_KEY).worksheet('–û–ø–∏—Ç—É–≤–∞–Ω–Ω—è')
    all_rows = polls_sheet.get_all_records()
    for idx, row in enumerate(all_rows):
        if str(row.get("poll_id", "")) == str(poll_id):
            options = [opt.strip() for opt in row["–≤–∞—Ä—ñ–∞–Ω—Ç–∏"].split(",")]
            answers = ", ".join([options[i] for i in selected if i < len(options)])
            time = datetime.now().strftime("%Y-%m-%d %H:%M")
            polls_sheet.update(f"F{idx+2}", username)     # F: username
            polls_sheet.update(f"G{idx+2}", answers)      # G: –≤—ñ–¥–ø–æ–≤—ñ–¥—å
            polls_sheet.update(f"H{idx+2}", time)         # H: —á–∞—Å
            break

